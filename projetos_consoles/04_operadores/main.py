# declaracao de variaveis
x = 5
y = 2

# operacoes
soma = x + y
subtracao = x - y
multiplicacao = x*y
divisao = x/y
resto = x%y
potencia = x**y

# soma de dados
print("A soma de {} e {} é {}.".format(x, y, soma))
print(f"A subtracao de {x} e {y} é {subtracao}.")
print(f"A multiplicacao de {x} e {y} é {multiplicacao}.")
print(f"A divisao de {x} e {y} é {divisao}.")
print(f"O resto da divisao de {x} e {y} é {resto}.")
print(f"{x} elevado a {x} é {potencia}")



